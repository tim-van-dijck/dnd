<p>You touch a dead humanoid or a piece of a dead humanoid. Provided that the creature has been dead no longer than 10 days, the spell forms a new adult body for it and then calls the soul to enter that body. If the target's soul isn't free or willing to do so, the spell fails.</p>
<p>The magic fashions a new body for the creature to inhabit, which likely causes the creature's race to change. The DM rolls a d 100 and consults the following table to determine what form the creature takes when restored to life, or the DM chooses a form.</p>
<ul>
    <li><b>01-04</b> Dragonborn</li>
    <li><b>05-13</b> Dwarf, hill</li>
    <li><b>14-21</b> Dwarf, mountain</li>
    <li><b>22-25</b> Elf, dark</li>
    <li><b>26-34</b> Elf, high</li>
    <li><b>35-42</b> Elf, wood</li>
    <li><b>43-46</b> Gnome, forest</li>
    <li><b>47-52</b> Gnome, rock</li>
    <li><b>53-56</b> Half-elf</li>
    <li><b>57-60</b> Half-orc</li>
    <li><b>61-68</b> Halfling, lightfoot</li>
    <li><b>69-76</b> Halfling, stout</li>
    <li><b>77-96</b> Human</li>
    <li><b>97-00</b> Tiefling</li>
</ul>
<p>The reincarnated creature recalls its former life and experiences. It retains the capabilities it had in its original form, except it exchanges its original race for the new one and changes its racial traits accordingly.</p>