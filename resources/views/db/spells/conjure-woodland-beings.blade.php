<p>You summon fey creatures that appear in unoccupied spaces that you can see within range. Choose one of the following options for what appears:</p>
<ul>
    <li>One fey creature of challenge rating 2 or lower</li>
    <li>Two fey creatures of challenge rating 1 or lower</li>
    <li>Four fey creatures of challenge rating 1/2 or lower</li>
    <li>Eight fey creatures of challenge rating 1/4 or lower</li>
</ul>
<p>A summoned creature disappears when it drops to 0 hit points or when the spell ends.</p>
<p>The summoned creatures are friendly to you and your companions. Roll initiative for the summoned creatures as a group, which have their own turns. They obey any verbal commands that you issue to them (no action required by you). If you don't issue any commands to them, they defend themselves from hostile creatures, but otherwise take no actions.</p>
<p>The DM has the creatures' statistics.</p>