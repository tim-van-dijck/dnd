<p>
    You can see and hear a particular creature you choose that is on the same plane of existence as you.
    The target must make a wisdom saving throw, which is modified by how well you know the target and the sort of physical connection you have to it.
    If a target knows you're casting this spell, it can fail the saving throw voluntarily if it wants to be observed.
</p>
<h5>Knowledge & Save Modifier</h5>
<ul>
    <li><b>Secondhand:</b> (you have heard of the target) +5</li>
    <li><b>Firsthand:</b> (you have met the target) +0</li>
    <li><b>Familiar:</b> (you know the target well) -5</li>
</ul>
<h5>Connection & Save Modifier</h5>
<ul>
    <li><b>Likeness or picture:</b> -2</li>
    <li><b>Possession or garment:</b> -4</li>
    <li><b>Body part, lock of hair, bit of nail, or the like:</b> -10</li>
</ul>
<p>On a successful save, the target isn't affected, and you can't use this spell against it again for 24 hours.</p>
<p>On a failed save, the spell creates an invisible sensor within 10 feet of the target. You can see and hear through the sensor as if you were there. The sensor moves with the target, remaining within 10 feet of it for the duration. A creature that can see invisible objects sees the sensor as a luminous orb about the size of your fist.</p>
<p>Instead of targeting a creature, you can choose a location you have seen before as the target of this spell. When you do, the sensor appears at that location and doesn't move.</p>