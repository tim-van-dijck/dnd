<p>You pull wisps of shadow material from the Shadowfell to create a nonliving object of vegetable matter within range: soft goods, rope, wood, or something similar. You can also use this spell to create mineral objects such as stone, crystal, or metal. The object created must be no larger than a 5-foot cube, and the object must be of a form and material that you have seen before.</p>
<p>The duration depends on the object's material. If the object is composed of multiple materials, use the shortest duration.</p>
<ul>
    <li><b>Vegetable matter:</b> 1 day</li>
    <li><b>Stone or crystal:</b> 12 hours</li>
    <li><b>Precious metals:</b> 1 hour</li>
    <li><b>Gems:</b> 10 minutes</li>
    <li><b>Adamantine or mithral:</b> 1 minute</li>
</ul>
<p>Using any material created by this spell as another spell's material component causes that spell to fail.</p>